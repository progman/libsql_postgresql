#!/bin/bash
#-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
function do_it()
{
# create temp dir and files
	local LOCAL_TMPDIR="/tmp";
	if [ "${TMPDIR}" != "" ] && [ -d "${TMPDIR}" ];
	then
		LOCAL_TMPDIR="${TMPDIR}";
	fi


	local TMP1;
	TMP1="$(mktemp --tmpdir="${LOCAL_TMPDIR}" 2> /dev/null)";
	if [ "${?}" != "0" ];
	then
		echo "can't make tmp file";
		return 1;
	fi


	find ./src/ -type f | grep -v 'head.php' | grep -v 'sql_t.php' | sort >> "${TMP1}";


	echo "<?php" > libsql_postgresql.php


	cat src/head.php >> libsql_postgresql.php;
	cat src/sql_t.php >> libsql_postgresql.php;


	while read -r LINE;
	do

		cat "${LINE}" >> libsql_postgresql.php;

	done < "${TMP1}";


	echo -n "?>" >> libsql_postgresql.php


	rm -rf -- "${TMP1}" &> /dev/null;


	return 0;
}
#-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
do_it;
#-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
