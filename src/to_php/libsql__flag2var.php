function libsql__flag2var($table_name_short, $col_name, $flag_make_col_alias = true)
{
	if ($flag_make_col_alias === false)
	{
		return $table_name_short.".".$col_name."::int";
	}

	return $table_name_short.".".$col_name."::int AS ".$col_name;
}
