function libsql__text2var($table_name_short, $col_name, $flag_make_col_alias = true)
{
	if ($flag_make_col_alias === false)
	{
		return $table_name_short.".".$col_name;
	}

	return $table_name_short.".".$col_name."::text AS ".$col_name;
}
