function libsql__error($sql_handle)
{
	$error = @pg_last_error();

	if (($error == '') && (@pg_connection_status($sql_handle) !== PGSQL_CONNECTION_OK))
	{
		$error = "database connection lost";
	}

//	return drop_sql_injection($error);
	return $error;
}
