function libsql__ping($sql_handle)
{
	$result = new result_t(__FUNCTION__, __FILE__);


	$flag_connection = @pg_ping($sql_handle);
	if ($flag_connection === false)
	{
		$result->set_err(1, "sql error", libsql__error($sql_handle));
		return $result;
	}


	$result->set_ok();
	return $result;
}
